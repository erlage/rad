import 'package:rad/src/widgets/abstract/widget.dart';

/// The UnOrderedList widget (HTML's `ul` tag).
///
/// This HTML dom node represents an unordered list of items.
///
class UnOrderedList extends HTMLWidgetBase {
  const UnOrderedList({
    Key? key,
    String? id,
    String? title,
    String? style,
    String? classAttribute,
    int? tabIndex,
    bool? draggable,
    bool? contentEditable,
    Map<String, String>? dataAttributes,
    bool? hidden,
    String? onClickAttribute,
    String? innerText,
    Widget? child,
    List<Widget>? children,
    EventCallback? onClick,
  }) : super(
          key: key,
          id: id,
          title: title,
          style: style,
          classAttribute: classAttribute,
          tabIndex: tabIndex,
          draggable: draggable,
          contentEditable: contentEditable,
          dataAttributes: dataAttributes,
          hidden: hidden,
          onClickAttribute: onClickAttribute,
          innerText: innerText,
          child: child,
          children: children,
          onClick: onClick,
        );

  @nonVirtual
  @override
  String get widgetType => 'UnOrderedList';

  @override
  DomTagType get correspondingTag => DomTagType.unOrderedList;
}
